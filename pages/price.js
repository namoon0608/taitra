import Head from "next/head";
import Nav from "../Components/Nav";
import Footer from "../Components/Footer";
import Hero from "../Components/Hero";
import styles from "../styles/Home.module.scss";
import { serverSideTranslations } from "next-i18next/serverSideTranslations";
import { useTranslation } from "next-i18next";

export async function getServerSideProps({ locale }) {
    const options = {
        method: "POST",
        headers: {
            "Content-Type": "application/x-www-form-urlencoded",
        },
        body: new URLSearchParams({ lang: locale, show_id: "FD" }),
    };
    const res = await fetch(
        `${process.env.API_BASE_URL}getPriceTable`,
        options
    );
    const data = await res.json();
    const infoRes = await fetch(
        `${process.env.API_BASE_URL}getDiscountInfo`,
        options
    );
    const infoData = await infoRes.json();

    return {
        props: {
            ...(await serverSideTranslations(locale, ["common"])),
            priceList: data,
            info: infoData,
        },
    };
}

export default function Price(props) {
    const { t } = useTranslation();
    const priceList = props.priceList;
    let rows = [];
    let rowIndex = 0;
    for (let [i, item] of priceList.items.entries()) {
        rowIndex = Math.floor(i / 30);
        if (!rows[rowIndex]) {
            rows[rowIndex] = [];
        }
        rows[rowIndex].push(item);
    }
    const getList = (data) => {
        let lists = [];
        for (let i = 0; i < 30; i++) {
            if (data[i] === undefined) {
                lists.push(
                    <tr key={data[i]}>
                        <td className={styles.item}></td>
                        <td></td>
                        <td className={styles.itemName}></td>
                        <td className={styles.price}></td>
                    </tr>
                );
            } else {
                lists.push(
                    <tr key={data[i]}>
                        <td className={styles.item}>{data[i].index}</td>
                        <td>{data[i].type}</td>
                        <td className={styles.itemName}>{data[i].name}</td>
                        <td className={styles.price}>{data[i].price}</td>
                    </tr>
                );
            }
        }
        return lists;
    };

    return (
        <div className={styles.container}>
            <Head>
                <title>TAITRA</title>
                <meta
                    name="description"
                    content="Generated by create next app"
                />
                <link rel="icon" href="/img/logo.svg" />
            </Head>

            <Nav />
            <Hero info={props.info}>
                <h3>價目表</h3>
                <div
                    className={
                        rows.length > 1
                            ? styles.priceContain
                            : styles.priceOneContain
                    }
                >
                    {rows.length === 0 ? (
                        <div>Loading...</div>
                    ) : (
                        rows.map((row, index) => (
                            <table key={index}>
                                <thead>
                                    <tr>
                                        <th>項次</th>
                                        <th>用電項目</th>
                                        <th className={styles.itemName}>
                                            項目名稱
                                        </th>
                                        <th>定價(含稅)</th>
                                    </tr>
                                </thead>
                                <tbody>{getList(row)}</tbody>
                            </table>
                        ))
                    )}
                </div>
            </Hero>
            <Footer />
        </div>
    );
}
